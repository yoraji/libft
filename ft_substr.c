#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = ft_calloc((len + 1), sizeof(char));
	if (!ptr || !s)
		return (NULL);
	if (start > ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	while (i < len)
	{
		ptr[i] = s[i + start];
		i++;
	}
	return (ptr);
}
